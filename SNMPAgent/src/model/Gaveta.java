package model;

public class Gaveta {
	private int cervejas;
	private int ovos;
	private int leite;
	private int indexGaveta;
	
	public Gaveta(){
		
	}

	public int getCervejas() {
		return cervejas;
	}
	public void setCervejas(int cervejas) {
		this.cervejas = cervejas;
	}
	public int getOvos() {
		return ovos;
	}
	public void setOvos(int ovos) {
		this.ovos = ovos;
	}
	public int getLeite() {
		return leite;
	}
	public void setLeite(int leite) {
		this.leite = leite;
	}
	public int getIndexGaveta() {
		return indexGaveta;
	}
	public void setIndexGaveta(int indexGaveta) {
		this.indexGaveta = indexGaveta;
	}
	
	
}
